#import "Fonts.h"

@implementation Fonts

+(UIFont*) getHelveticaNeueOfSize:(CGFloat)size
{
    return [UIFont fontWithName:@"Helvetica Neue" size:size];
}

+(UIFont*) getBebasNeueOfSize:(CGFloat)size
{
    return [UIFont fontWithName:@"Bebas Neue" size:size];
}

@end
